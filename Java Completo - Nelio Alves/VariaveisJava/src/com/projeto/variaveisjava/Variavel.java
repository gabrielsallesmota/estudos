package com.projeto.variaveisjava;

public class Variavel {
	
	//Variaveis
	//Byte - Números - 1,2,10 (de -128 até 127)
	//int - Números - 1000, 23003, 2, 1
	//Long - Numeros - 1415418541871, 198148187418
	//Float - Numeros Decimais - 1.1234, 2.1234
	//double - Numeros Decimais - 1.2345678
	//bloolean - Valores Booleanos - true/false
	//String - Textos - "café", "escola"

	public static void main(String[] args) {
		
		int minhaVariavelInt = 128415215;
		byte minhaVariavelByte = 10;
		long minhaVariavelLong = 1111118470;
		boolean minhaVariavelBoolean = false;

		System.out.println (minhaVariavelByte);
		System.out.println (minhaVariavelInt);
		System.out.println (minhaVariavelLong);
		System.out.println (minhaVariavelBoolean);
	}

}
